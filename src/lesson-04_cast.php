<?php

// 整数型 (int)
(int)$variable; // 変数$variableを整数型にキャストします。

// 浮動小数点型 (float)
(float)$variable; // 変数$variableを浮動小数点型にキャストします。

// 文字列型 (string)
(string)$variable; // 変数$variableを文字列型にキャストします。

// ブール型 (bool)
(bool)$variable; // 変数$variableをブール型にキャストします。

// 配列型 (array)
(array)$variable; // 変数$variableを配列型にキャストします。

// オブジェクト型 (object)
(object)$variable; // 変数$variableをオブジェクト型にキャストします。

// NULL型
unset($variable); // 変数$variableをNULLにします。